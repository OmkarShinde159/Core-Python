# WAP to calculate area & perimeter of rectangle
# area of rec = length * breadth
# perimeter of rec = 2(length + breadth)

len = int(input("Enter the length of RECTANGLE : "))
bd = int(input("Enter the breadth of RECTANGLE : "))

area = len * bd
print("The area of RECTANGLE is : ", area)

per = 2*(len + bd)
print("The per of RECTANGLE is : ", per)

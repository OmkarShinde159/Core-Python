#WAP to find week day based on given number
#1 - Monday
#2 - Tuesday
#3 - Wednesday
#4 - Thursday
#5 - Friday
#6 - Saturday
#7 - Sunday

week_day = input("Wnter week day number: ")

if week_day == "1":
    print("Monday")
elif week_day == "2":
    print("Tuesday")
elif week_day == "3":
    print("Wednesday")
elif week_day == "4":
    print("Thursday")
elif week_day == "5":
    print("Friday")
elif week_day == "6":
    print("Saturday")
elif week_day == "7":
    print("Sunday")
else:
    print("Invalid Number...Please enter number between 1-7")
    
